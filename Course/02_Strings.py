firstName = "joHn"
lastName = "cEna"

# Concatenate above 2 variables.
fullName = firstName+' '+lastName
# Print the value which was concatenated.
print(fullName)
print(fullName.upper()) # Make variable uppercase.
print(fullName.lower()) # Make variable lowercase.
print(fullName.title()) # Make first letter of variable in a word uppercase.
print(fullName.split()) # Make each string as a separate value delimited by space (default).
print(fullName[2]) # Print the related index in output.



